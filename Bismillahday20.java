public class ContohPenjumlahanByte {
    public static void main(String[] args) {
        // Menggunakan tipe data referensi Byte
        Byte byte1 = 10;
        Byte byte2 = 20;
        Byte byte3 = 30;

        // Menghitung total
        int total = byte1 + byte2 + byte3;

        // Menampilkan hasil
        System.out.println("Total nilai: " + total);
    }
}