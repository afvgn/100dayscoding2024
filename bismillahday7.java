public class InisialisasiSederhana {
    public static void main(String[] args) {
        // Inisialisasi variabel dengan tipe data yang berbeda
        int angka = 10;               
        double desimal = 20.5;         
        char karakter = 'A';           
        String teks = "Hello, Java!";  
        boolean benar = true;   

        // Menampilkan nilai dari setiap variabel
        System.out.println("Nilai dari angka: " + angka);
        System.out.println("Nilai dari desimal: " + desimal);
        System.out.println("Nilai dari karakter: " + karakter);
        System.out.println("Nilai dari teks: " + teks);
        System.out.println("Nilai dari boolean: " + benar);
    }
}
