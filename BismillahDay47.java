public class OperatorLogikaNegasi {
    public static void main(String[] args) {
        boolean nilaiBenar = true;
        boolean nilaiSalah = false;

        System.out.println("Nilai asli: " + nilaiBenar);
        System.out.println("Nilai setelah negasi: " + !nilaiBenar);

        System.out.println("Nilai asli: " + nilaiSalah);
        System.out.println("Nilai setelah negasi: " + !nilaiSalah);
    }
}
